/* INSTRUCTIONS

Write reverseList function that simply reverses lists.

*/
/* SOLUTION

*/
function reverseList(arr) {
	return arr.reverse();
}

// TEST CASES
console.log(reverseList([]), []);
console.log(reverseList([1, 2, 3]), [3, 2, 1]);
